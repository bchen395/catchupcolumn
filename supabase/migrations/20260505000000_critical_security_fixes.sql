-- ============================================================
-- 20260505000000_critical_security_fixes.sql
-- Three security fixes:
--   1. Lock down group_members INSERT so users cannot self-promote
--      to moderator. Joins must go through a new RPC that requires
--      a valid invite code.
--   2. Restore case-insensitive invite-code lookup with auth check
--      (regressed in 20260426000007).
--   3. Set search_path on the SECURITY DEFINER helpers used by RLS
--      so they cannot be shadowed via a hostile search_path.
-- ============================================================

-- --------------------------------------------------------
-- 1. RLS helpers: add `set search_path = public`
--    `create or replace` keeps existing policies intact.
-- --------------------------------------------------------
create or replace function public.is_group_member(p_group_id uuid, p_user_id uuid)
returns boolean
language sql
security definer
stable
set search_path = public
as $$
  select exists (
    select 1 from public.group_members
    where group_id = p_group_id and user_id = p_user_id
  );
$$;

create or replace function public.is_group_moderator(p_group_id uuid, p_user_id uuid)
returns boolean
language sql
security definer
stable
set search_path = public
as $$
  select exists (
    select 1 from public.group_members
    where group_id = p_group_id and user_id = p_user_id and role = 'moderator'
  );
$$;

-- --------------------------------------------------------
-- 2. group_members INSERT policy: deny direct inserts.
--    Legitimate inserts come from:
--      a) handle_new_group() trigger (security definer, bypasses RLS)
--      b) join_group_by_invite_code() RPC below (security definer)
-- --------------------------------------------------------
drop policy if exists "Users can join a group" on public.group_members;

create policy "Members can only be created via RPC or trigger"
  on public.group_members for insert
  to authenticated
  with check (false);

-- --------------------------------------------------------
-- 3. Atomic invite-code join RPC.
--    - Requires authentication
--    - Resolves group by invite code (case-insensitive)
--    - Inserts membership as 'contributor' only (caller cannot
--      choose role)
--    - Returns the resolved group_id so the client can navigate
-- --------------------------------------------------------
create or replace function public.join_group_by_invite_code(p_invite_code text)
returns uuid
language plpgsql
security definer
set search_path = public
as $$
declare
  v_group_id uuid;
begin
  if auth.uid() is null then
    raise exception 'Not authenticated' using errcode = 'PGRST301';
  end if;

  if p_invite_code is null or length(trim(p_invite_code)) = 0 then
    raise exception 'invalid_invite_code' using errcode = 'P0002';
  end if;

  select id into v_group_id
  from public.groups
  where lower(invite_code) = lower(trim(p_invite_code))
  limit 1;

  if v_group_id is null then
    raise exception 'invalid_invite_code' using errcode = 'P0002';
  end if;

  insert into public.group_members (group_id, user_id, role)
  values (v_group_id, auth.uid(), 'contributor')
  on conflict (group_id, user_id) do nothing;

  return v_group_id;
end;
$$;

grant execute on function public.join_group_by_invite_code(text) to authenticated;

-- --------------------------------------------------------
-- 4. Restore case-insensitive invite-code lookup with auth check.
--    Migration 20260426000007 regressed this to upper(...) which
--    never matches the lowercase hex codes generated by
--    gen_random_uuid()::text. Also re-adds the auth.uid() guard
--    that 20260426000003 had introduced.
--    Keeps the timezone column added in 20260426000006.
-- --------------------------------------------------------
drop function if exists public.find_group_by_invite_code(text);

create or replace function public.find_group_by_invite_code(p_invite_code text)
returns table (
  id              uuid,
  name            text,
  description     text,
  cover_image_url text,
  publish_day     int,
  publish_time    time,
  timezone        text,
  created_by      uuid,
  invite_code     text,
  created_at      timestamptz
)
language plpgsql
security definer
stable
set search_path = public
as $$
begin
  if auth.uid() is null then
    raise exception 'Not authenticated' using errcode = 'PGRST301';
  end if;

  return query
    select g.id, g.name, g.description, g.cover_image_url, g.publish_day,
           g.publish_time, g.timezone, g.created_by, g.invite_code, g.created_at
    from public.groups g
    where lower(g.invite_code) = lower(trim(p_invite_code))
    limit 1;
end;
$$;
